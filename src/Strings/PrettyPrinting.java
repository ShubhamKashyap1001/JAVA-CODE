public class PrettyPrinting{
    public static void main(String[] args) {
        float a = 4567.1234f;
        System.out.printf("Formatted number is %.2f",a);

        System.out.println();
        System.out.printf("Pie is %.3f",Math.PI);

        System.out.println();
        System.out.printf("My name is %s and I am %s ", "shubham" , "student");
    }
}